  final String species;
  final String type;
  final String description;
  final double confidence;
  final String habitat;
  final String size;
  final String shape;

  DetectedDiatom({
    required this.species,
    required this.type,
    required this.description,
    required this.confidence,
    required this.habitat,
    required this.size,
    required this.shape,
  });
}
